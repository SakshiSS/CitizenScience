package com.model;

public class Rectangle {
	
	int rid;
	double nlat;
	double wlng;
	double slat;
	double elng;
	
	public int getrid() {
		return rid;
	}
	public void setrid(int rid) {
		this.rid=rid;
	}
	public double getNlat() {
		return nlat;
	}
	public void setNlat(double nlat) {
		this.nlat = nlat;
	}
	public double getWlng() {
		return wlng;
	}
	public void setWlng(double wlng) {
		this.wlng = wlng;
	}
	public double getSlat() {
		return slat;
	}
	public void setSlat(double slat) {
		this.slat = slat;
	}
	public double getElng() {
		return elng;
	}
	public void setElng(double elng) {
		this.elng = elng;
	}
	

}
